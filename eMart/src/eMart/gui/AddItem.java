    private boolean validateInput() {
        if (txtQuantity.getText().isEmpty() || txtOurPrice.getText().isEmpty() || txtPrice.getText().isEmpty() || txtName.getText().isEmpty() || txtCompanyName.getText().isEmpty()) {
            return false;
        }
        return true;
    }
