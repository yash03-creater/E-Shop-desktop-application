    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!validateInput()) {
            JOptionPane.showMessageDialog(null, "please fill proper input");
            return;
        }
        int l = jcTax.getSelectedItem().toString().length();
        String taxx = jcTax.getSelectedItem().toString().substring(0, l - 1);
        ProductPojo pp = new ProductPojo();
        pp.setProductId(txtId.getText().trim());
        pp.setCompanyName(txtCompanyName.getText().trim());
        pp.setProductName(txtName.getText().trim());
        pp.setProductPrice(Double.parseDouble(txtPrice.getText().trim()));
        pp.setOurPrice(Double.parseDouble(txtOurPrice.getText().trim()));
        pp.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
        pp.setTax(Integer.parseInt(taxx.trim()   ));
        try {
            boolean res = ProductDao.addProduct(pp);
            if (res) {
                JOptionPane.showMessageDialog(null, "item added to stock");
                BarCode_IMG_Generator.createImage(txtId.getText().trim(),txtId.getText().trim());
                clearText();
                loadId();
                return;
            } else {
                JOptionPane.showMessageDialog(null, "Not added");
                return;
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnAddActionPerformed

